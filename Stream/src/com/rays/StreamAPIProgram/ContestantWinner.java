package com.rays.StreamAPIProgram;

public class ContestantWinner {

	String phoneNo;
	String name;

	public ContestantWinner(String phoneNo, String name) {

		this.phoneNo = phoneNo;
		this.name = name;

	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public String getName() {
		return name;
	}

}
